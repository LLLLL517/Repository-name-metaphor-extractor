"""
比喻提取器 - 简单版
功能：从文章里找出所有比喻句（把A比作B / A像B / A是B）
作者：进击的硬件菌
"""

# 导入正则表达式模块（Python自带，不需要安装）
import re

# ============================================
# 文章内容（已经帮你填好了测试文章）
# 三个引号之间的所有内容都会被当作一个字符串
# ============================================
article_text = """
写技术文章的时候，我喜欢用比喻来把抽象的东西讲清楚。

比如把 platformio.ini 比作“国家政策”——它定了规矩，所有人都得照着来，改起来还麻烦得要死。
把 VS Code 插件比作“城市运行”——平时感觉不到，一旦没了，整个开发环境就瘫痪了。

调试舵机那会儿，我像堂吉诃德一样打了半年风车。舵机是个假风车，怎么调都转不对；ESP32 是个坏风车，代码烧进去就没反应；串口乱码是个转反了的风车，看得我头晕。

SolidWorks 像个够不着的风车，画好了图导不出来；流氓软件是个成精的风车，装一个蹦出来五个。

代码写多了，我发现编译器也会“罢工”，动不动就“抗议”——明明上一秒还好好的，下一秒就报错。这时候我就知道，它又在“发脾气”了。
"""

# ============================================
# 第一步：提取比喻
# re.findall(规则, 文章) 会找出所有匹配的内容
# ============================================

# 规则1：把A比作“B”
# 解释：把(任意字符)比作“任意字符”
pattern1 = r'把([^比]+)比作[““”"]([^”"]+)[”"]'
metaphors1 = re.findall(pattern1, article_text)

# 规则2：A像B
# 解释：(2-20个字符)像(2-20个字符)
pattern2 = r'([^，。]{2,20})像([^，。]{2,20})'
metaphors2 = re.findall(pattern2, article_text)

# 规则3：A是B
# 解释：(2-20个字符)是(2-20个字符)
pattern3 = r'([^，。]{2,20})是([^，。]{2,20})'
metaphors3 = re.findall(pattern3, article_text)

# ============================================
# 第二步：合并所有比喻
# ============================================
all_metaphors = metaphors1 + metaphors2 + metaphors3

# 去重：去掉重复的比喻
unique_metaphors = []
for m in all_metaphors:
    if m not in unique_metaphors:
        unique_metaphors.append(m)

# ============================================
# 第三步：输出结果
# ============================================
print("=" * 50)
print("比喻提取结果")
print("=" * 50)

if len(unique_metaphors) == 0:
    print("没有找到比喻句")
else:
    for a, b in unique_metaphors:
        # strip() 去掉前后多余的空格
        print(f"  {a.strip()} → {b.strip()}")

print("=" * 50)
print(f"共找到 {len(unique_metaphors)} 个比喻")